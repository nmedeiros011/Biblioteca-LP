        try {
            cadastrar.cadLivro(
                    txtTitulo.getText(),
                    txtAutor.getText(),
                    txtIsbn.getText(),
                    Integer.parseInt(txtQtd.getText()));
            JOptionPane.showMessageDialog(null, "Cadastro do livro '" + txtTitulo.getText() + "' foi realizado com sucesso!");
            this.dispose();
        } catch (IOException ex) {
            //Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
