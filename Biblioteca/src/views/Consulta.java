    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        biblioteca.Consultar consultar = new biblioteca.Consultar();
        int combo = cbCampo.getSelectedIndex() + 1;
        try {
            models.Livro[] l = consultar.consLivro(txtBusca.getText(), combo);
            lblResultado.setText("<html>");
            for (models.Livro a : l) {
                lblResultado.setText(
                        lblResultado.getText()
                        + "ISBN: " + a.getISBN()
                        + " - TÃ­tulo: " + a.getTitulo()
                        + " - Autor: " + a.getAutor()
                        + "<br/>");
            }
            lblResultado.setText(lblResultado.getText() + "<html/>");
        } catch (IOException ex) {
            //Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

